#ifndef _galaxian_dipswitches_h_
#define _galaxian_dipswitches_h_

// IN0 Cabinet
#define GALAXIAN_DIP_UPRIGHT      0b00000000
#define GALAXIAN_DIP_COCKTAIL     0b00100000

// IN1 Coinage
#define GALAXIAN_DIP_COIN_1C1P    0b00000000
#define GALAXIAN_DIP_COIN_2C1P    0b01000000

// IN2 Bonus life
#define GALAXIAN_DIP_BONUS_7000   0b00000000
#define GALAXIAN_DIP_BONUS_10000  0b00000001
#define GALAXIAN_DIP_BONUS_12000  0b00000010
#define GALAXIAN_DIP_BONUS_20000  0b00000011
#define GALAXIAN_DIP_LIVES_2      0b00000000
#define GALAXIAN_DIP_LIVES_3      0b00000100

#define GALAXIAN_DIP_IN0  (GALAXIAN_DIP_UPRIGHT)
#define GALAXIAN_DIP_IN1  (GALAXIAN_DIP_COIN_1C1P)
#define GALAXIAN_DIP_IN2  (GALAXIAN_DIP_BONUS_7000 | GALAXIAN_DIP_LIVES_3)

#endif
