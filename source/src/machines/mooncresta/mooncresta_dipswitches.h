#ifndef MOONCRESTA_DIPSWITCHES_H
#define MOONCRESTA_DIPSWITCHES_H

// IN2: Coinage
#define MOONCRESTA_DIP_COIN1_1C1P    0b00000000
#define MOONCRESTA_DIP_COIN1_2C1P    0b00000001
#define MOONCRESTA_DIP_COIN1_3C1P    0b00000010
#define MOONCRESTA_DIP_COIN1_4C1P    0b00000011
#define MOONCRESTA_DIP_COIN2_1C1P    0b00000000
#define MOONCRESTA_DIP_COIN2_1C2P    0b00000100
#define MOONCRESTA_DIP_COIN2_1C3P    0b00001000
#define MOONCRESTA_DIP_COIN2_FREE    0b00001100
#define MOONCRESTA_DIP_IN2_UNUSED    0b11110000

// IN1: Language
#define MOONCRESTA_DIP_IN1_UNUSED    0b00100000
#define MOONCRESTA_DIP_BONUS_30000   0b00000000
#define MOONCRESTA_DIP_BONUS_50000   0b01000000
#define MOONCRESTA_DIP_ENGLISH       0b10000000
#define MOONCRESTA_DIP_JAPANESE      0b00000000

// IN0 bit 6: Cabinet (MAME: 0x00=Upright, 0x20=Cocktail)
#define MOONCRESTA_DIP_IN0_UNUSED   0b11000000
#define MOONCRESTA_DIP_UPRIGHT      0b00000000
#define MOONCRESTA_DIP_COCKTAIL     0b00100000

#define MOONCRESTA_DIP_IN0  (MOONCRESTA_DIP_IN0_UNUSED | MOONCRESTA_DIP_UPRIGHT)
#define MOONCRESTA_DIP_IN1  (MOONCRESTA_DIP_IN1_UNUSED | MOONCRESTA_DIP_ENGLISH)
#define MOONCRESTA_DIP_IN2  (MOONCRESTA_DIP_IN2_UNUSED | MOONCRESTA_DIP_COIN1_1C1P )

#endif
